def are_arrays_equal(arr1, arr2):
    # 检查数组的尺寸
    if len(arr1) != len(arr2) or len(arr1[0]) != len(arr2[0]):
        return False

    # 比较数组元素
    for i in range(len(arr1)):
        for j in range(len(arr1[0])):
            if arr1[i][j] != arr2[i][j]:
                print(f"i:{i},j:{j}")
                return False

    return True

